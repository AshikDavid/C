# include <stdio.h>
/*This program is to print different datatype objects in c */
void main(){
    printf("%d \n",35);//decimal
    printf("%ld \n",23232324);//large decimal
    printf("%f \n",35.5);//float
    printf("%lf \n",34.4444444444);//large float
    printf("%c \n",'a');//character
    printf("%s \n","hello world"); //string
}